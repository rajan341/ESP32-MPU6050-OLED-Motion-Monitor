void oled_write_string(uint8_t page, uint8_t col, const char *str)
{
    if (str == NULL || page >= 8) return;
    
    oled_cmd(0xB0 + page);
    oled_cmd(0x02 + (col & 0x0F));
    oled_cmd(0x10 + ((col >> 4) & 0x0F));
    
    for (int i = 0; str[i] != '\0'; i++) {
        char c = str[i];
        // Handle ASCII characters from space (32) to z (122)
        if (c >= 32 && c <= 122) {
            int idx = c - 32; // Convert ASCII to font table index
            for (int j = 0; j < 5; j++) {
                oled_data(font_table[idx][j]);
            }
        } else {
            // For unknown characters, print space
            oled_data(0x00);
        }
    }
}

void oled_display_number(uint8_t page, uint8_t col, int32_t num)
{
    char buffer[16];
    sprintf(buffer, "%ld", num);
    oled_write_string(page, col, buffer);
}

void oled_display_text(const char *text, uint8_t page)
{
    oled_clear();
    if (text != NULL) {
        oled_write_string(page, 0, text);
    }
}