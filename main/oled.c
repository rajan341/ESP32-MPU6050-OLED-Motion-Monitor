#include "oled.h"
#include "driver/i2c.h"
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"

#define OLED_ADDR 0x3C
#define I2C_NUM I2C_NUM_0

static void oled_cmd(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};

    i2c_master_write_to_device(
        I2C_NUM,
        OLED_ADDR,
        data,
        2,
        100 / portTICK_PERIOD_MS);
}

void oled_init(void)
{
    vTaskDelay(100 / portTICK_PERIOD_MS);

    oled_cmd(0xAE);
    oled_cmd(0xD5);
    oled_cmd(0x80);
    oled_cmd(0xA8);
    oled_cmd(0x3F);
    oled_cmd(0xD3);
    oled_cmd(0x00);
    oled_cmd(0x40);
    oled_cmd(0xAD);
    oled_cmd(0x8B);
    oled_cmd(0xA1);
    oled_cmd(0xC8);
    oled_cmd(0xDA);
    oled_cmd(0x12);
    oled_cmd(0x81);
    oled_cmd(0x7F);
    oled_cmd(0xA4);
    oled_cmd(0xA6);
    oled_cmd(0xAF);
}

void oled_clear()
{
    uint8_t buffer[132];
    buffer[0] = 0x40;

    for(int i=1;i<132;i++)
        buffer[i]=0x00;

    for(int page=0; page<8; page++)
    {
        oled_cmd(0xB0 + page);
        oled_cmd(0x02);
        oled_cmd(0x10);

        i2c_master_write_to_device(
            I2C_NUM,
            OLED_ADDR,
            buffer,
            132,
            100 / portTICK_PERIOD_MS);
    }
}

static void oled_data(uint8_t data_byte)
{
    uint8_t data[2] = {0x40, data_byte};

    i2c_master_write_to_device(
        I2C_NUM,
        OLED_ADDR,
        data,
        2,
        100 / portTICK_PERIOD_MS);
}

